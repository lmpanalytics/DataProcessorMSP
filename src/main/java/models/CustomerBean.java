package models;

/**
 *
 * @author SEPALMM
 */
public class CustomerBean {

    private String name;
    private int age;
    private double income;

    public CustomerBean() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public double getIncome() {
        return income;
    }

    public void setIncome(double income) {
        this.income = income;
    }

}
